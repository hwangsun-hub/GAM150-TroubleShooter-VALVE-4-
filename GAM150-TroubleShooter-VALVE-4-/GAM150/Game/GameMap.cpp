void Game::GameMap::Load(Game::Player& player,MapName mapname) {
	currentMapName = mapname;
	std::vector<Door*>doors;
	LoadMap(currentMapName);
	std::vector<std::vector<int>>& currentMap = maps[static_cast<int>(currentMapName)];
	for (int y = 0; y < currentMap.size(); y++) {
		for (int x = 0; x < currentMap[y].size(); x++) {
			
			if (maps_for_saw[static_cast<int>(currentMapName)][y][x] == 70 //Saw
				) {
				int x_for_row[4] = { 1,-1,0,0 };
				int y_for_column[4] = { 0,0,1,-1 };

				
				int size = 0;
				Vector2 direction{ 0,0 };
				for (int i = 0; i < 4; i++)
				{
					int sx = x + x_for_row[i];
					int sy = y + y_for_column[i];
					if (sx > 0 && sx < maps_for_saw[static_cast<int>(currentMapName)][y].size() && sy>0 && sy < maps_for_saw[static_cast<int>(currentMapName)].size())
					{
						if (maps_for_saw[static_cast<int>(currentMapName)][sy][sx] > 70 &&
							maps_for_saw[static_cast<int>(currentMapName)][sy][sx] < 80) {
							size = maps_for_saw[static_cast<int>(currentMapName)][sy][sx]-70;
							direction = { float(x_for_row[i]) ,float(y_for_column[i]) };
							break;
						}
					}
					

				}
				
				objects.push_back(
					new Saw(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(maps_for_saw[static_cast<int>(currentMapName)][y][x] - 70),
						false,
						size,
						direction,
						player
					)
				);
			}
			else if (maps_for_saw[static_cast<int>(currentMapName)][y][x] == 75 //Glitched Saw
				) {
				
				objects.push_back(
					new Saw(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(maps_for_saw[static_cast<int>(currentMapName)][y][x] - 75),
						true,
						0,
						Vector2 {0,0},
						player
					)
				);
			}

			
			if (currentMap[y][x] >= 0 && //Block
				currentMap[y][x] <= 24) {
				objects.push_back(
					new Block(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(currentMap[y][x]),
						false
					));
			}

			else if (currentMap[y][x] >= 25 && //Glitched Block
				currentMap[y][x] <= 49
				) {
				objects.push_back(
					new Block(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(currentMap[y][x] - 25),
						true
					));

			}

			else if (currentMap[y][x] == 50 //Spike
				) {
				objects.push_back(
					new Spike(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(currentMap[y][x] - 50),
						false,
						false
						)
				);
			}
			else if (currentMap[y][x] == 51 //upside down Spike
				) {
				objects.push_back(
					new Spike(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(currentMap[y][x] - 50),
						false,
						true
					)
				);
			}
			else if (currentMap[y][x] == 52 //Glitched Spike
				) {
				objects.push_back(
					new Spike(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(currentMap[y][x] - 52),
						true,
						false
					)
				);
			}
			else if (currentMap[y][x] == 53 //Glitched upsidedown Spike
				) {
				objects.push_back(
					new Spike(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(currentMap[y][x] - 52),
						true,
						true
					)
				);
			}
			else if (currentMap[y][x] == 60//Door
				) {
				Door* door = new Door (
					Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
					(currentMap[y][x] - 60),
					false
				);
				doors.push_back(door);
				objects.push_back(door);
			}
			else if (currentMap[y][x] == 61//Glitch Door
				) {
				Door* door = new Door(
					Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
					(currentMap[y][x] - 61),
					true
				);
				doors.push_back(door);
				objects.push_back(door);
			}
			else if (currentMap[y][x] >= 65 && //Number Block
				currentMap[y][x] <= 66
				) {
				objects.push_back(
					new Block(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(currentMap[y][x] - 65),
						false,
						ObjectID::ID::NUMBER_BLOCK
					));

			}
			else if (currentMap[y][x] >= 67 && //Glitched Number Block
				currentMap[y][x] <= 68
				) {
				objects.push_back(
					new Block(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(currentMap[y][x] - 67),
						true,
						ObjectID::ID::NUMBER_BLOCK
					));

			}
			else if (currentMap[y][x] == 80 //Box
				) {
				objects.push_back(
					new Box(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE }
					)
				);
			}
			else if (currentMap[y][x] == 81 //GlitchBox
				) {
				objects.push_back(
					new Box(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						true
					)
				);
			}
			else if (currentMap[y][x] == 99 //player_start_position
				) {
				player_start_position = { static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE+ TILE_SIZE/2};
			}
			else if (maps[static_cast<int>(currentMapName)][y][x] == 54 //Flag
				) {
				objects.push_back(
					new Flag(
						Vector2{ static_cast<float>(x) * TILE_SIZE, static_cast<float>(y) * TILE_SIZE },
						(maps[static_cast<int>(currentMapName)][y][x] - 54),
						false
					)
				);
			}
		}
	}
	if (doors.size() == 2 && doors[0] && doors[1]) {
		doors[0]->SetAnotherDoor(doors[1]);
		doors[1]->SetAnotherDoor(doors[0]);
	}
	for (Engine::GameObject* obj : objects) {
		obj->Load();
	}
}
void Game::GameMap::Unload() {
	for (Engine::GameObject* obj : objects)
	{
		delete obj;
	}
	for (Trouble* t : troubles) {
		delete t;
	}
	troubles.clear();
	objects.clear();

	
}

void Game::GameMap::Update(Game::Player& player, Game::Player& player2, Dialogue& dialogue, double dt, bool IsPlayer2Maked) {

	if (!IsPlayer2Maked) {
		Update2(player, dialogue, dt);
		return;
	}

	if (player.CheckTroubleShoot()) {
			//making touble
		troubles.push_back(
			new Trouble(
				player.GetPosition(),
				player.GetIsLookingRight()
			)
		);
		troubles.push_back(
			new Trouble(
				player2.GetPosition(),
				player2.GetIsLookingRight()
			)
		);
	}

	
	player.SetIsOnGround(false);
	player.SetCanJump(true);
	player2.SetIsOnGround(false);
	player2.SetCanJump(true);

	for (Engine::GameObject* obj : objects) {
		player.HandleCollision(obj, dt);
	}
	for (Engine::GameObject* obj : objects) {
		player.CorrectCollision(obj, dt, objects);

	}
	for (Engine::GameObject* obj : objects) {
		player2.HandleCollision(obj, dt);
	}
	for (Engine::GameObject* obj : objects) {
		player2.CorrectCollision(obj, dt, objects);

	}
	for (Engine::GameObject* obj : objects) {
		obj->Update(dt);
	}

	Box::ResolveCollisions(objects);

	for (Trouble* trouble : troubles) {
		for (Engine::GameObject* obj : objects) {
			trouble->Update(obj, dt);
		}
	}
	
	
	
	for (int i = static_cast<int>(objects.size()) - 1; i >= 0; i--)
	{
		if (objects[i]->GetUnload())
		{
			delete objects[i];
			objects.erase(objects.begin() + i);
		}
	}

	for (int i = static_cast<int>(troubles.size()) - 1; i >= 0; i--)
	{
		if (troubles[i]->GetUnload())
		{
			delete troubles[i];
			troubles.erase(troubles.begin() + i);
		}
	}
	

}


void Game::GameMap::draw() {
	for (Engine::GameObject* obj : objects) {
		obj->Draw();
	}
	for (Trouble* trouble : troubles) {
		trouble->Draw();
	}
}



